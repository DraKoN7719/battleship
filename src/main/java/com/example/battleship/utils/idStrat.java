package com.example.battleship.utils;

public class idStrat {
    int id;
    int strat;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStrat() {
        return strat;
    }

    public void setStrat(int strat) {
        this.strat = strat;
    }

    public idStrat(int id, int strat) {
        this.id = id;
        this.strat = strat;
    }
}
